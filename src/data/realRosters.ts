/** Seed row — values in EUR (fee ≈ valueM * 1e6, weekly wage ≈ wageK * 1000). */
export type PlayerSeed = {
  name: string;
  age: number;
  nat: string;
  pos: PitchPos;
  ovr: number;
  /** Market value in millions EUR */
  valueM: number;
  /** Weekly wage in thousands EUR */
  wageK: number;
};

export type ClubSeed = {
  id: string;
  name: string;
  shortName: string;
  leagueId: LeagueId;
  prestige: number;
  difficulty: RebuildDifficulty;
  boardGoal: BoardGoal;
  /** Transfer budget millions EUR */
  transferBudgetM: number;
  /** Annual wage budget millions EUR */
  wageBudgetM: number;
  colors: { primary: string; secondary: string };
  players: PlayerSeed[];
};

function p(
  name: string,
  age: number,
  nat: string,
  pos: PitchPos,
  ovr: number,
  valueM: number,
  wageK: number,
): PlayerSeed {
  return { name, age, nat, pos, ovr, valueM, wageK };
}

